package com.split;
/**
 * @author vinay.pawar
 *
 */
public class Divisional {
	
	private Integer section1;
	private Integer section2;
	/**
	 * @param section1
	 * @param section2
	 */
	public Divisional(Integer section1, Integer section2) {
		super();
		this.section1 = section1;
		this.section2 = section2;
	}
	/**
	 * @return the section1
	 */
	public Integer getSection1() {
		return section1;
	}
	/**
	 * @param section1 the section1 to set
	 */
	public void setSection1(Integer section1) {
		this.section1 = section1;
	}
	/**
	 * @return the section2
	 */
	public Integer getSection2() {
		return section2;
	}
	/**
	 * @param section2 the section2 to set
	 */
	public void setSection2(Integer section2) {
		this.section2 = section2;
	}
}
